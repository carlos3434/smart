const vm = new Vue({
    el: '#main',
    data: {
        user:{},
        roles: [],
        accion:''
    },

    methods: {
        /**boton de modal Guardar*/
        guardarUser: function () {
            if (vm.accion=='nuevo') {
                Users.store();
            } else {
                Users.update(vm.user.id);
            }
        },
        /**boton llama a modal, nuevo user */
        storeUser: function () {
            $("#userModal").modal();
            vm.accion = 'nuevo';
            vm.user = {};
            $selectRoles.val([]).trigger("change");
        },
        roles: function(){
            Roles.all();
        },
    }
});


